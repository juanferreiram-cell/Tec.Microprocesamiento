ISR(USART_RX_vect) {
	uint8_t recibido = UDR0;
	
	switch(recibido) {
		case 'C':
		case 'c':
		animacion_actual = 0;
		comando_nuevo = 1;
		prueba_colores();
		uart_imprimir("Esperando comandos\r\n");
		break;
		
		case '1':
		animacion_actual = 1;
		comando_nuevo = 1;
		uart_imprimir("Reproduciendo Animacion Pacman\r\n");
		break;
		
		default:
		uart_imprimir("ERROR: Comando invalido\r\n");
		uart_imprimir("Comandos: C, 1\r\n");
		break;
	}
}

void cargar_frame_anim1(uint8_t numero_frame) {
	if(numero_frame >= NUM_FRAMES_ANIM1) return;
	
	for(uint16_t i = 0; i < NUM_LEDS; i++) {
		leds[i].g = pgm_read_byte(&pacman[numero_frame][i * 3]);
		leds[i].r = pgm_read_byte(&pacman[numero_frame][i * 3 + 1]);
		leds[i].b = pgm_read_byte(&pacman[numero_frame][i * 3 + 2]);
	}
}

void delay_no_bloqueante(uint16_t ms) {
	for(uint16_t i = 0; i < ms; i++) {
		_delay_ms(1);
		if(comando_nuevo) {
			comando_nuevo = 0;
			return;
		}
	}
}

int main(void) {
	ws2812_inicializar();
	uart_inicializar();
	
	sei();
	
	limpiar_matriz();
	ws2812_actualizar();
	
	uart_imprimir("Sistema de Matriz LED RGB - Version 2\r\n");
	uart_imprimir("Comandos disponibles:\r\n");
	uart_imprimir("  C - Prueba de colores (Rojo, Verde, Azul)\r\n");
	uart_imprimir("  1 - Reproducir Animacion Pacman\r\n");
	uart_imprimir("Esperando comandos\r\n\r\n");
	
	uint8_t contador_frames = 0;
	
	while(1) {
		switch(animacion_actual) {
			case 0:
			limpiar_matriz();
			ws2812_actualizar();
			delay_no_bloqueante(100);
			break;
			
			case 1:
			cargar_frame_anim1(contador_frames);
			ws2812_actualizar();
			
			contador_frames++;
			if(contador_frames >= NUM_FRAMES_ANIM1) {
				contador_frames = 0;
				} else {
				delay_no_bloqueante(400);
			}
			break;
			
			default:
			limpiar_matriz();
			ws2812_actualizar();
			delay_no_bloqueante(250);
			break;
		}
		
		if(comando_nuevo) {
			contador_frames = 0;
			comando_nuevo = 0;
		}
	}
	
	return 0;
}