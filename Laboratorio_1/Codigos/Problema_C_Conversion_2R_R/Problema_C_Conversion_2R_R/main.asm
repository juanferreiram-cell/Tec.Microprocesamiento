; Senial 4 (256 muestras) con DAC R-2R en PORTD usando Timer2
; Microcontrolador: ATmega328P y Frecuencia_CPU = 16 MHz
; Fout ? 1 kHz (256 muestras,  fs = 256 kHz)
; Fs = F_CPU / (presc * (1 + OCR2A)) = 16x10^6 / (1 * (1 + 62)) ? 253.97 kHz

.include "m328pdef.inc"

.cseg
.org 0x0000
    rjmp Inicio                  ; Reset vector
.org OC2Aaddr
    rjmp Interrupcion            ; TIMER2_COMPA

; Registros de trabajo
.def aux1  = r16
.def aux2  = r17
.def valor = r18                 ; Byte leido de LUT

.equ OCR2A_VAL = 62              ; Se varia segun la Fout buscada

Inicio:
    ; Stack
    ldi aux1, high(RAMEND)
    out SPH, aux1
    ldi aux1, low(RAMEND)
    out SPL, aux1

    ; GPIO: PORTD salida (8 bits al R-2R)
    ldi aux1, 0xFF
    out DDRD, aux1
    ldi aux1, 0x00
    out PORTD, aux1

    ; Timer2 en CTC
    ldi aux1, OCR2A_VAL
    sts OCR2A, aux1              ; Valor de comparacion
    ldi aux1, (1<<WGM21)
    sts TCCR2A, aux1             ; CTC con OCR2A
    ldi aux1, (1<<OCF2A)
    out TIFR2, aux1              ; Limpiar OCF2A 
    ldi aux1, (1<<OCIE2A)
    sts TIMSK2, aux1             ; Habilitar interrupcion COMPA
    ldi aux1, (1<<CS20)
    sts TCCR2B, aux1             ; Prescaler = 1 

    ; Puntero de LUT en FLASH 
    ldi ZL, low(LUT*2)
    ldi ZH, high(LUT*2)

    sei                           ; Habilitar interrupciones

Bucle:
    rjmp Bucle                  

; Interrupcion Timer2 COMPA
Interrupcion:
    
    lpm  valor, Z+                ; Leer siguiente muestra
    out  PORTD, valor             ; Salida a 2R-R

    ; Volver al inicio si Z llego al fin de LUT
    ldi  aux1,  low(LUTFin*2)
    ldi  aux2,  high(LUTFin*2)
    cp   ZL, aux1
    cpc  ZH, aux2
    brlo seguir
    ldi  ZL, low(LUT*2)
    ldi  ZH, high(LUT*2)
seguir:
    reti

